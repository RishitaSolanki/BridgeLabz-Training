//Datatype

  using System;

  public class Datatype{

  public static void Main(string[] args){
  
  int a=10;
    Console.WriteLine(a);
  
  long b=1000;
    Console.WriteLine(b);

  
  float c= 1.02f;
    Console.WriteLine(c);

  
  double d=22.05;
    Console.WriteLine(d);

  
  char e='G';
    Console.WriteLine(e);

  
  bool isf= false;
    Console.WriteLine(isf);

  
  String name="Rishita";
    Console.WriteLine(name);

  
  //Implicit Typecasting
  
  int num=10;
  double pointnum=num;
    Console.WriteLine(pointnum);

  
  //Explicit Typecasting
  
  double n=11.55;
  int m=(int) n;
    Console.WriteLine(m);

  

}

}