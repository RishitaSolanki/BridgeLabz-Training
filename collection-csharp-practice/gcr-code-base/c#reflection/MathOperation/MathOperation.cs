using System;
using System.Collections.Generic;
using System.Text;

namespace ProjectFive.Reflection.MathOperation
{
    public class MathOperation
    {

        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Subtract(int a, int b)
        {
            return a - b;
        }

        public int Multiply(int a, int b)
        {
            return a * b;
        }
    }
}

